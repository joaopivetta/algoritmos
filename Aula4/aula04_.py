anoNasc = int(input("Que ano você nasceu? \n"))
anoAtual = int(input("Qual o ano atual? \n"))
idade = anoAtual - anoNasc

print("sua idade é: ", idade, "anos")
print("Sua idade em meses é: ", idade * 12, "meses")
print("Sua idade em dias é: ", idade * 365, "Dias")
print("Sua idade em semanas é: ", idade * 53, "Semanas")
